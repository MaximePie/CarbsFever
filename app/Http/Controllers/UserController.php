     * Display the specified resource.
     *
     * @param  string  $username
     * @return JsonResponse
     */
    public function show($username)
    {
        /** @var User $user */
        $user = User::query()->where('name', '=', $username)->firstOrFail();
        $average = $user->averageCarbsPerDay();
        $averageAndTargetComparison = $average - $user->target;
        return new JsonResponse([
            'average' => $average,
            'targetComparison' => $averageAndTargetComparison,
            'user' => $user,
        ]);
    }

